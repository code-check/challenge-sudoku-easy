package sudoku

import "testing"

func TestIsValid(t *testing.T) {
	if IsValid("782614359439825176651937428293471864568392715147568293326749581975183662814256937") {
		t.Error("found 'true' expected 'false'")
	}
	if IsValid("782614359439825176651937428293471864568392715147568293326749581975183642814256937") {
		t.Error("found 'true' expected 'false'")
	}
	if !IsValid("782614359439825176651937428293471865568392714147568293326749581975183642814256937") {
		t.Error("found 'false' expected 'true'")
	}
	if IsValid("483921654967345821251876493548132976729564138136798245372689514814253769695417382") {
		t.Error("found 'true' expected 'false'")
	}
	if !IsValid("954213687617548923832796541763851294128974365549362178281637459475129836396485712") {
		t.Error("found 'false' expected 'true'")
	}
	if IsValid("954213687617548923832796541763851294128974365549362178281637549475129836396485712") {
		t.Error("found 'true' expected 'false'")
	}
}